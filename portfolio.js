const root = document.documentElement;
const metaDescription = document.querySelector('#page-description');
const revealNodes = document.querySelectorAll('.reveal');
const tiltNodes = document.querySelectorAll('[data-tilt]');
const parallaxNodes = document.querySelectorAll('[data-parallax]');
const navLinks = Array.from(document.querySelectorAll('.topnav a[href^="#"]'));
const languageButtons = Array.from(document.querySelectorAll('[data-lang-trigger]'));
const textNodes = Array.from(document.querySelectorAll('[data-i18n]'));
const htmlNodes = Array.from(document.querySelectorAll('[data-i18n-html]'));
const attrNodes = Array.from(document.querySelectorAll('[data-i18n-attr]'));
const atlasSteps = Array.from(document.querySelectorAll('[data-atlas-step]'));
const atlasNodes = Array.from(document.querySelectorAll('[data-atlas-node]'));
const prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;
const supportedLanguages = new Set(Object.keys(translations));
const storageKey = 'portfolioLanguage';

let currentLanguage = resolveInitialLanguage();
let atlasCycleId = null;

function resolveInitialLanguage() {
  const queryLanguage = new URLSearchParams(window.location.search).get('lang');
  if (supportedLanguages.has(queryLanguage)) {
    return queryLanguage;
  }

  try {
    const storedLanguage = window.localStorage.getItem(storageKey);
    if (supportedLanguages.has(storedLanguage)) {
      return storedLanguage;
    }
  } catch {
    // Ignore storage access failures.
  }

  return navigator.language.toLowerCase().startsWith('ru') ? 'ru' : 'en';
}

function t(key) {
  if (Object.prototype.hasOwnProperty.call(translations[currentLanguage] || {}, key)) {
    return translations[currentLanguage][key];
  }

  if (Object.prototype.hasOwnProperty.call(translations.en, key)) {
    return translations.en[key];
  }

  return key;
}

function syncLanguageButtons() {
  languageButtons.forEach((button) => {
    const isActive = button.dataset.langTrigger === currentLanguage;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });
}

function applyTranslations() {
  document.documentElement.lang = currentLanguage;
  document.body.dataset.language = currentLanguage;
  document.title = t('meta.title');

  if (metaDescription) {
    metaDescription.setAttribute('content', t('meta.description'));
  }

  textNodes.forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  htmlNodes.forEach((node) => {
    node.innerHTML = t(node.dataset.i18nHtml);
  });

  attrNodes.forEach((node) => {
    const instructions = node.dataset.i18nAttr.split(';');
    instructions.forEach((instruction) => {
      const [attributeName, key] = instruction.split(':').map((value) => value.trim());
      if (attributeName && key) {
        node.setAttribute(attributeName, t(key));
      }
    });
  });

  syncLanguageButtons();

  try {
    window.localStorage.setItem(storageKey, currentLanguage);
  } catch {
    // Ignore storage access failures.
  }

  const url = new URL(window.location.href);
  url.searchParams.set('lang', currentLanguage);
  window.history.replaceState({}, '', url);
}

function updateScrollProgress() {
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const progress = scrollable > 0 ? window.scrollY / scrollable : 0;
  root.style.setProperty('--scroll-progress', progress.toFixed(4));
}

function updateSpotlight(x, y) {
  root.style.setProperty('--pointer-x', `${x}px`);
  root.style.setProperty('--pointer-y', `${y}px`);
}

function updateParallax() {
  if (prefersReducedMotion) {
    parallaxNodes.forEach((node) => node.style.setProperty('--parallax-shift', '0px'));
    return;
  }

  const viewportCenter = window.innerHeight / 2;

  parallaxNodes.forEach((node) => {
    const speed = Number(node.dataset.parallax || 0);
    const rect = node.getBoundingClientRect();
    const delta = rect.top + rect.height / 2 - viewportCenter;
    node.style.setProperty('--parallax-shift', `${(-delta * speed).toFixed(2)}px`);
  });
}

if (prefersReducedMotion) {
  revealNodes.forEach((node) => node.classList.add('is-visible'));
} else {
  const revealObserver = new IntersectionObserver((entries, intersectionObserver) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) {
        return;
      }

      entry.target.classList.add('is-visible');
      intersectionObserver.unobserve(entry.target);
    });
  }, {
    threshold: 0.16,
    rootMargin: '0px 0px -8% 0px',
  });

  revealNodes.forEach((node) => revealObserver.observe(node));
}

tiltNodes.forEach((node) => {
  node.addEventListener('pointermove', (event) => {
    if (prefersReducedMotion) {
      return;
    }

    const rect = node.getBoundingClientRect();
    const relativeX = (event.clientX - rect.left) / rect.width - 0.5;
    const relativeY = (event.clientY - rect.top) / rect.height - 0.5;
    const rotateY = relativeX * 8;
    const rotateX = relativeY * -7;
    node.style.setProperty('--tilt-y', `${rotateY.toFixed(2)}deg`);
    node.style.setProperty('--tilt-x', `${rotateX.toFixed(2)}deg`);
  });

  node.addEventListener('pointerleave', () => {
    node.style.setProperty('--tilt-y', '0deg');
    node.style.setProperty('--tilt-x', '0deg');
  });
});

const sectionMap = navLinks
  .map((link) => {
    const target = document.querySelector(link.getAttribute('href'));
    return target ? { link, target } : null;
  })
  .filter(Boolean);

if (sectionMap.length > 0) {
  const navObserver = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      const matched = sectionMap.find(({ target }) => target === entry.target);

      if (!matched || !entry.isIntersecting) {
        return;
      }

      navLinks.forEach((link) => link.classList.remove('is-active'));
      matched.link.classList.add('is-active');
    });
  }, {
    threshold: 0.35,
    rootMargin: '-12% 0px -40% 0px',
  });

  sectionMap.forEach(({ target }) => navObserver.observe(target));
}

function setAtlasActive(stepId) {
  atlasSteps.forEach((step) => {
    step.classList.toggle('is-active', step.dataset.atlasStep === stepId);
  });

  atlasNodes.forEach((node) => {
    node.classList.toggle('is-active', node.dataset.atlasNode === stepId);
  });
}

function stopAtlasCycle() {
  if (atlasCycleId !== null) {
    window.clearInterval(atlasCycleId);
    atlasCycleId = null;
  }
}

function startAtlasCycle() {
  if (prefersReducedMotion || atlasSteps.length < 2) {
    return;
  }

  stopAtlasCycle();
  atlasCycleId = window.setInterval(() => {
    const currentIndex = atlasSteps.findIndex((step) => step.classList.contains('is-active'));
    const nextIndex = currentIndex === -1 ? 0 : (currentIndex + 1) % atlasSteps.length;
    setAtlasActive(atlasSteps[nextIndex].dataset.atlasStep);
  }, 2800);
}

if (atlasSteps.length > 0) {
  setAtlasActive(atlasSteps[0].dataset.atlasStep);
  startAtlasCycle();

  atlasSteps.forEach((step) => {
    const stepId = step.dataset.atlasStep;
    step.addEventListener('pointerenter', () => {
      stopAtlasCycle();
      setAtlasActive(stepId);
    });
    step.addEventListener('focusin', () => {
      stopAtlasCycle();
      setAtlasActive(stepId);
    });
    step.addEventListener('pointerleave', startAtlasCycle);
    step.addEventListener('focusout', startAtlasCycle);
  });
}

let frameRequested = false;

function queueMotionUpdate() {
  if (frameRequested) {
    return;
  }

  frameRequested = true;
  window.requestAnimationFrame(() => {
    updateScrollProgress();
    updateParallax();
    frameRequested = false;
  });
}

languageButtons.forEach((button) => {
  button.addEventListener('click', () => {
    const nextLanguage = button.dataset.langTrigger;
    if (!supportedLanguages.has(nextLanguage) || nextLanguage === currentLanguage) {
      return;
    }

    currentLanguage = nextLanguage;
    applyTranslations();
  });
});

applyTranslations();
updateScrollProgress();
updateParallax();
updateSpotlight(window.innerWidth * 0.72, window.innerHeight * 0.18);

window.addEventListener('scroll', queueMotionUpdate, { passive: true });
window.addEventListener('resize', queueMotionUpdate);

if (!prefersReducedMotion) {
  window.addEventListener('pointermove', (event) => {
    updateSpotlight(event.clientX, event.clientY);
  }, { passive: true });
}
