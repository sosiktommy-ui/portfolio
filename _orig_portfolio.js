function resolveInitialLanguage() {
  const queryLanguage = new URLSearchParams(window.location.search).get('lang');
  if (supportedLanguages.has(queryLanguage)) {
    return queryLanguage;
  }

  try {
    const storedLanguage = window.localStorage.getItem(storageKey);
    if (supportedLanguages.has(storedLanguage)) {
      return storedLanguage;
    }
  } catch {
    // Ignore storage access failures.
  }

  return navigator.language.toLowerCase().startsWith('ru') ? 'ru' : 'en';
}

function t(key) {
  if (Object.prototype.hasOwnProperty.call(translations[currentLanguage] || {}, key)) {
    return translations[currentLanguage][key];
  }

  if (Object.prototype.hasOwnProperty.call(translations.en, key)) {
    return translations.en[key];
  }

  return key;
}

function syncLanguageButtons() {
  languageButtons.forEach((button) => {
    const isActive = button.dataset.langTrigger === currentLanguage;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });
}

function applyTranslations() {
  document.documentElement.lang = currentLanguage;
  document.body.dataset.language = currentLanguage;
  document.title = t('meta.title');

  if (metaDescription) {
    metaDescription.setAttribute('content', t('meta.description'));
  }

  textNodes.forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  htmlNodes.forEach((node) => {
    node.innerHTML = t(node.dataset.i18nHtml);
  });

  attrNodes.forEach((node) => {
    const instructions = node.dataset.i18nAttr.split(';');
    instructions.forEach((instruction) => {
      const [attributeName, key] = instruction.split(':').map((value) => value.trim());
      if (attributeName && key) {
        node.setAttribute(attributeName, t(key));
      }
    });
  });

  syncLanguageButtons();

  try {
    window.localStorage.setItem(storageKey, currentLanguage);
  } catch {
    // Ignore storage access failures.
  }

  const url = new URL(window.location.href);
  url.searchParams.set('lang', currentLanguage);
  window.history.replaceState({}, '', url);
}

function updateScrollProgress() {
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const progress = scrollable > 0 ? window.scrollY / scrollable : 0;
  root.style.setProperty('--scroll-progress', progress.toFixed(4));
}

function updateSpotlight(x, y) {
  root.style.setProperty('--pointer-x', `${x}px`);
  root.style.setProperty('--pointer-y', `${y}px`);
}

function updateParallax() {
  if (prefersReducedMotion) {
    parallaxNodes.forEach((node) => node.style.setProperty('--parallax-shift', '0px'));
    return;
  }

  const viewportCenter = window.innerHeight / 2;

  parallaxNodes.forEach((node) => {
    const speed = Number(node.dataset.parallax || 0);
    const rect = node.getBoundingClientRect();
    const delta = rect.top + rect.height / 2 - viewportCenter;
    node.style.setProperty('--parallax-shift', `${(-delta * speed).toFixed(2)}px`);
  });
}

